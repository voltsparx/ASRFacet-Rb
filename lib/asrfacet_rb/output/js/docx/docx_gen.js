"use strict";

const fs = require("fs");
const {
  Document,
  Packer,
  Paragraph,
  Table,
  TableRow,
  TableCell,
  TextRun,
  HeadingLevel,
  AlignmentType,
  WidthType,
  BorderStyle,
  ImageRun,
  ShadingType,
} = require("docx");
const { loadPayload, requireOutputPath } = require("../shared/data_loader");
const { buildAllCharts } = require("../shared/chart_builder");
const { COLORS } = require("../shared/colors");
const buildCover = require("./sections/cover");
const buildSummary = require("./sections/summary");
const buildSubdomains = require("./sections/subdomains");
const buildIpsPorts = require("./sections/ips_ports");
const buildFindings = require("./sections/findings");
const buildJsEndpoints = require("./sections/js_endpoints");
const buildCharts = require("./sections/charts");

const hex = (color) => color.replace("#", "");

function heading1(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_1,
    spacing: { after: 200 },
  });
}

function heading2(text) {
  return new Paragraph({
    text,
    heading: HeadingLevel.HEADING_2,
    spacing: { before: 300, after: 160 },
  });
}

function heading3(text) {
  return new Paragraph({
    children: [
      new TextRun({
        text,
        bold: true,
        size: 26,
        color: hex(COLORS.accent),
      }),
    ],
    spacing: { before: 200, after: 100 },
  });
}

function para(text, opts = {}) {
  return new Paragraph({
    children: [
      new TextRun({
        text: String(text),
        color: hex(opts.color || COLORS.text),
        size: opts.size || 20,
        bold: opts.bold || false,
        italics: opts.italic || false,
      }),
    ],
    spacing: { after: opts.after || 80 },
    alignment: opts.align || AlignmentType.LEFT,
  });
}

function labelValue(label, value) {
  return new Paragraph({
    children: [
      new TextRun({
        text: `${label}: `,
        bold: true,
        size: 20,
        color: hex(COLORS.muted),
      }),
      new TextRun({
        text: String(value),
        size: 20,
        color: hex(COLORS.text),
      }),
    ],
    spacing: { after: 80 },
  });
}

function hr() {
  return new Paragraph({
    border: {
      bottom: {
        style: BorderStyle.SINGLE,
        size: 6,
        color: hex(COLORS.border),
      },
    },
    spacing: { before: 160, after: 160 },
    text: "",
  });
}

function emptyState(message) {
  return para(message, { color: COLORS.muted, italic: true });
}

function tableCell(text, opts = {}) {
  return new TableCell({
    children: [
      new Paragraph({
        children: [
          new TextRun({
            text: String(text),
            bold: opts.bold || false,
            size: opts.size || 18,
            color: hex(opts.color || COLORS.text),
          }),
        ],
      }),
    ],
    shading: opts.shading
      ? { fill: hex(opts.shading), type: ShadingType.CLEAR }
      : undefined,
    width: opts.width
      ? { size: opts.width, type: WidthType.DXA }
      : { size: 1, type: WidthType.AUTO },
  });
}

function headerRow(columns) {
  return new TableRow({
    children: columns.map((column) =>
      tableCell(column, {
        bold: true,
        shading: "#1F2937",
        color: COLORS.muted,
      })
    ),
    tableHeader: true,
  });
}

function dataRow(cells, opts = {}) {
  return new TableRow({
    children: cells.map((cell, index) =>
      tableCell(cell, {
        color: opts.colors && opts.colors[index] ? opts.colors[index] : COLORS.text,
      })
    ),
  });
}

function makeTable(headerCols, rows) {
  return new Table({
    rows: [headerRow(headerCols), ...rows],
    width: { size: 9000, type: WidthType.DXA },
  });
}

function imageBlock(buffer, label) {
  if (!buffer) {
    return para(`[Chart: ${label} - no data]`, {
      color: COLORS.muted,
      italic: true,
    });
  }

  return new Paragraph({
    children: [
      new ImageRun({
        data: buffer,
        transformation: { width: 480, height: 240 },
        type: "png",
      }),
    ],
    spacing: { before: 120, after: 120 },
  });
}

function buildFooter(data) {
  return [
    para(`Generated by ASRFacet-Rb v${data.meta.version}`, {
      align: AlignmentType.CENTER,
      color: COLORS.muted,
    }),
    para("For authorized security research only.", {
      align: AlignmentType.CENTER,
      color: COLORS.muted,
    }),
  ];
}

async function main() {
  const payloadPath = process.argv[2];
  const outputPath = requireOutputPath(process.argv[3]);
  const data = loadPayload(payloadPath);

  console.log("[*] Building charts...");
  const charts = await buildAllCharts(data.charts || {});

  const helpers = {
    heading1,
    heading2,
    heading3,
    para,
    labelValue,
    hr,
    emptyState,
    tableCell,
    headerRow,
    dataRow,
    makeTable,
    imageBlock,
  };

  console.log("[*] Assembling DOCX...");
  const doc = new Document({
    title: `ASRFacet-Rb - ${data.meta.target}`,
    description: "Reconnaissance report generated by ASRFacet-Rb",
    styles: {
      default: {
        document: {
          run: {
            font: "Segoe UI",
            size: 20,
            color: hex(COLORS.text),
          },
        },
      },
    },
    sections: [
      {
        children: [
          ...buildCover(data, helpers),
          ...buildSummary(data, helpers),
          ...buildCharts(charts, helpers),
          ...buildSubdomains(data, helpers),
          ...buildIpsPorts(data, helpers),
          ...buildFindings(data, helpers),
          ...buildJsEndpoints(data, helpers),
          ...buildFooter(data),
        ],
      },
    ],
  });

  const buffer = await Packer.toBuffer(doc);
  fs.writeFileSync(outputPath, buffer);
  console.log(`[ok] DOCX written (docx.js) -> ${outputPath}`);
}

main().catch((error) => {
  console.error(`[error] docx_gen.js failed: ${error.message}`);
  process.exit(1);
});
